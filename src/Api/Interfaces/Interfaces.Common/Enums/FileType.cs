namespace Rhyous.EntityAnywhere.Interfaces
{
    /// <summary>A common enumeration of file types.</summary>
    public enum FileType
    {
        /// <summary>A file of an unknown type.</summary>
        Unknown,
        /// <summary>An Audio file.</summary>
        Audio,
        /// <summary>A document file, like word, excel, powerpoint, etc.</summary>
        Document,
        /// <summary>Any file type.</summary>
        Ebook,
        /// <summary>Any file type.</summary>
        File,
        /// <summary>An image or art file.</summary>
        Image,
        /// <summary>Any video file.</summary>
        Video,
        /// <summary>Any zip file.</summary>
        Zip
    }
}